const numbers = [30, 75, 85, 90, 20, 10]

//printing
console.log(numbers[4])    // 20

//push
numbers.push(35);
console.log(numbers)  //added 35 to the list of array
//pop
numbers.pop();  
console.log(numbers) //removed 35
//shift
numbers.shift()
console.log(numbers)   // removed 30
//unshift
numbers.unshift(44)
console.log(numbers) //added 44 from the front
